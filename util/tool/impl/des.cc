CDes::CDes()
{
    memset(SubKey,0,sizeof(SubKey));
    memset(deskey,0,16);
    Is3DES = false;
}

// 字节转换函数
void CDes::ByteToBit(bool *Out, const char *In, int bits)
{
    for(int i=0; i<bits; ++i)
        Out[i] = (In[i>>3]>>(i&7)) & 1;
}

// 比特转换函数
void CDes::BitToByte(char *Out, const bool *In, int bits)
{
    memset(Out, 0, bits>>3);
    for(int i=0; i<bits; ++i)
        Out[i>>3] |= In[i]<<(i&7);
}


// 变换函数
void CDes::Transform(bool *Out, bool *In, const char *Table, int len)
{
    bool Tmp[128];

    for(int i=0; i<len; ++i)
        Tmp[i] = In[ Table[i]-1 ];
    memcpy(Out, Tmp, len);
}

// 异或函数的实现
void CDes::Xor(bool *InA, const bool *InB, int len)
{
    for(int i=0; i<len; ++i)
        InA[i] ^= InB[i];
}

// 轮转函数
void CDes::RotateL(bool *In, int len, int loop)
{
    char Tmp[256];

    memcpy(Tmp, In, loop);
    memcpy(In, In+loop, len-loop);
    memcpy(In+len-loop, Tmp, loop);
}

/******************************************************************************/
//	名称：S_func
//	功能：S 盒代替
//  参数：
//	返回：
//  备注：私有函数，不用检查指针是否为空

/******************************************************************************/
void CDes::S_func(bool Out[32], const bool In[48])
{
    for(int i=0,j,k; i<8; ++i,In+=6,Out+=4) 
    {
        j = (In[0]<<1) + In[5];
        k = (In[1]<<3) + (In[2]<<2) + (In[3]<<1) + In[4];
		ByteToBit(Out, &S_Box[i][j][k], 4);
    }
}

/******************************************************************************/
//	名称：F_func
//	功能：f 函数
//  参数：
//	返回：
//  备注：私有函数，不用检查指针是否为空

/******************************************************************************/
void CDes::F_func(bool In[32], const bool Ki[48])
{
    bool MR[48];

    Transform(MR, In, Extension_Table, 48);
    Xor(MR, Ki, 48);
    S_func(In, MR);

    Transform(In, In, P_Table, 32);
}



/******************************************************************************/
//	名称：SetSubKey
//	功能：由输入密钥设置16圈子密钥
//  参数：
//	返回：
//  备注：私有函数，不用检查指针是否为空

/******************************************************************************/
void CDes::SetSubKey(PSubKey pSubKey, const char Key[8])
{
    //static bool K[64], *KL=&K[0], *KR=&K[28];
    bool K[64], *KL=&K[0], *KR=&K[28];

    // 将密钥字节组分解成密钥位组
    ByteToBit(K, Key, 64); 
    // 密钥变换
    Transform(K, K, PCK_Table, 56); 

	//由56位密钥产生48位子密钥
    for(int i=0; i<16; ++i) 
    {
        // 循环左移
        RotateL(KL, 28, LOOP_Table[i]);
        RotateL(KR, 28, LOOP_Table[i]);

        // 压缩变换
        Transform((*pSubKey)[i], K, PCC_Table, 48); 
    }
}


/******************************************************************************/
//	名称：SetSubKey
//	功能：由输入密钥串设置子密钥
//  参数：KeyStr为0结尾的密钥串，长度<=16，可为任意字符
//	返回：设置成功返回true，否则返回false
//  备注：当密钥长度>8时，将设置第2密钥，且置3次DES标志
//        密钥长度大于16时，只取前16位

/******************************************************************************/
void CDes::SetSubKey(const char* KeyStr)
{
    //保存密钥(超过16，只保存前16位)
    int len = strlen(KeyStr);
    len = (len > 16) ? 16 : len;
	memset(deskey, 0, 16);
	memcpy(deskey, KeyStr, len);

    //设置第1密钥
	SetSubKey(&SubKey[0], &deskey[0]);

    Is3DES = false;
    if(len > 8)
    {
        //置3次DES标志
        Is3DES = true;

        //设置第2密钥
        SetSubKey(&SubKey[1], &deskey[8]);
    }
}

/******************************************************************************/
//	名称：RandKeyStr
//	功能：产生随机密钥串
//  参数：
//	返回：KeyStr地址

/******************************************************************************/
char* CDes::RandKeyStr(char KeyStr[9])
{
    //产生随机数
    srand(rand());
    for(int i=0; i<8; ++i)
    {
        KeyStr[i] = rand()%256;
    }

    while( !( KeyStr[7] && 0xf0 ) )
        KeyStr[7] = rand()%256;

    KeyStr[8] = '\0';

    // 这样可以进行连接操作
    return KeyStr;
}


/******************************************************************************/
//	名称：DES
//	功能：加/解密
//  参数：Type―ENCRYPT:加密,DECRYPT:解密
//	返回：
//  备注：私有函数，不用检查指针是否为空

/******************************************************************************/
void CDes::DES(char Out[8], char In[8], const PSubKey pSubKey, bool Type)
{
    //static bool M[64], tmp[32], *Li=&M[0], *Ri=&M[32];
    bool M[64], tmp[32], *Li=&M[0], *Ri=&M[32];

    //将输入字节组分解成位组
    ByteToBit(M, In, 64);
    //初始变换
    Transform(M, M, IP_Table, 64);

    if( Type == ENCRYPT )  //加密变换
    {
        for(int i=0; i<16; ++i)
        {
            memcpy(tmp, Ri, 32);
            F_func(Ri, (*pSubKey)[i]); // 调用F函数
            Xor(Ri, Li, 32);
            memcpy(Li, tmp, 32);
        }
    }
    else  //解密变换
    {
        for(int i=15; i>=0; --i)
        {
            memcpy(tmp, Li, 32);
            F_func(Li, (*pSubKey)[i]);
            Xor(Li, Ri, 32);
            memcpy(Ri, tmp, 32);
        }
	}
    Transform(M, M, IPR_Table, 64);
    BitToByte(Out, M, 64);
}

/******************************************************************************/
//	名称：Encrypt
//	功能：加密
//  参数：len―数据长度，必须是8的倍数；KeyStr为0结尾的密钥串，长度<=16，可为任意字符
//        KeyStr默认值为0，如果你在调用前设置过子密钥，则可省略该参数。例:
//        SetSubKey(KeyStr);Encrypt(data,data,len);或:Encrypt(data,data,len,KeyStr);
//	返回：加密成功返回true，否则返回false
//  备注：当密钥长度>8时，系统自动使用3次DES加密

/******************************************************************************/
bool  CDes::Encrypt(char *Out,char *In,int len,const char *KeyStr) //加密
{
    if( !( Out != NULL && In != NULL && KeyStr != NULL) ) 
    {
        return false;
    }

	SetSubKey(KeyStr);

    if( !Is3DES)  //1次DES
    {
         for(int i=0,j=len>>3; i<j; ++i)
         {
             DES(Out,In,&SubKey[0], ENCRYPT);
             Out += 8; In  += 8;
         }
    }
    else
	{   // 3次DES E-D-E
        for(int i=0,j=len>>3; i<j; ++i)
        {
            DES(Out,In, &SubKey[0],ENCRYPT);
            DES(Out,Out,&SubKey[1],DECRYPT);
            DES(Out,Out,&SubKey[0],ENCRYPT);
            Out += 8; In += 8;
		}
    }

    return true;
}

/******************************************************************************/
//	名称：Decrypt
//	功能：解密
//  参数：len―数据长度，必须是8的倍数；KeyStr为0结尾的密钥串，长度<=16，可为任意字符
//        KeyStr默认值为0，如果你在调用前设置过子密钥，则可省略该参数。例:
//        SetSubKey(KeyStr);Decrypt(data,data,len);或:Decrypt(data,data,len,KeyStr);
//	返回：解密成功返回true，否则返回false
//  备注：当密钥长度>8时，系统自动使用3次DES解密

/******************************************************************************/
bool  CDes::Decrypt(char *Out,char *In,int len,const char *KeyStr) //解密
{
    if( !( Out != NULL && In != NULL && KeyStr != NULL) ) 
    {
        return false;
    }
    
	SetSubKey(KeyStr);

    if( !Is3DES )
    {   // 1次DES
        for(int i=0,j=len>>3; i<j; ++i)
        {
            DES(Out,In,&SubKey[0],DECRYPT);
            Out += 8; In += 8;
        }
    }
    else
    {   // 3次DES D-E-D
        for(int i=0,j=len>>3; i<j; ++i)
        {
            DES(Out,In, &SubKey[0],DECRYPT);
            DES(Out,Out,&SubKey[1],ENCRYPT);
            DES(Out,Out,&SubKey[0],DECRYPT);
            Out += 8; In += 8;
        }
    }
    
	return true;
}


